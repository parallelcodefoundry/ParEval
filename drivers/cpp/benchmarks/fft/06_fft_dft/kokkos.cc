// /* Compute the discrete fourier transform of x. Store the result in output.
//    Use Kokkos to compute in parallel. Assume Kokkos is already initialized.
//    Example:
// 
//    input: [1, 4, 9, 16]
//    output: [30+0i, -8-12i, -10-0i, -8+12i]

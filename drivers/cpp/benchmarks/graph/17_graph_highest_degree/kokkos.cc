void fillRandomUndirectedGraph(std::vector<int> &A, size_t N) {
    std::fill(A.begin(), A.end(), 0);
    for (int i = 0; i < N; i += 1) {
        A[i * N + i] = 0;
        for (int j = i + 1; j < N; j += 1) {
            A[i * N + j] = rand() % 2;
            A[j * N + i] = A[i * N + j];
        }
    }
}

